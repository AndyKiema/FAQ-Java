public class swappingnumbers {
    public static void main(String[] args) {
        int a =10;
        int b =20;
        System.out.println("The value of a is "+a);
        System.out.println("The value of b is "+b);
        int x;
        x=a;
        a=b;
        b=x;
        System.out.println("The value of a is "+a);
        System.out.println("The value of b is "+b);
    }
}
